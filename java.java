public class java{
    public static void Halfpyramid(int n){
        for(int i=0;i<n;i++){
            for(int j=0;j<n-i;j++){
                System.out.println("");
            }
            System.out.print("*");
        }
    }
    public static void main(String args[]){
        System.out.println(0);
    }
}